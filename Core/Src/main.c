void user_i2c_init(void) {
    hi2c1.Instance              = I2C1;
    hi2c1.Init.Timing           = 0x2000090E;
    hi2c1.Init.OwnAddress1      = flash_arg[0] << 1;
    hi2c1.Init.AddressingMode   = I2C_ADDRESSINGMODE_7BIT;
    hi2c1.Init.DualAddressMode  = I2C_DUALADDRESS_DISABLE;
    hi2c1.Init.OwnAddress2      = 0;
    hi2c1.Init.OwnAddress2Masks = I2C_OA2_NOMASK;
    hi2c1.Init.GeneralCallMode  = I2C_GENERALCALL_DISABLE;
    hi2c1.Init.NoStretchMode    = I2C_NOSTRETCH_DISABLE;
    if (HAL_I2C_Init(&hi2c1) != HAL_OK) {
        Error_Handler();
    }
    /** Configure Analogue filter
     */
    if (HAL_I2CEx_ConfigAnalogFilter(&hi2c1, I2C_ANALOGFILTER_ENABLE) !=
        HAL_OK) {
        Error_Handler();
    }
    /** Configure Digital filter
     */
    if (HAL_I2CEx_ConfigDigitalFilter(&hi2c1, 0) != HAL_OK) {
        Error_Handler();
    }
}

void write_to_flash(void) {
    writeMessageToFlash(flash_arg, 8);
}

void read_from_flash(void) {
    uint16_t diff_load = 0;
    uint32_t diff_adc  = 0;

    if (!(readPackedMessageFromFlash(flash_arg, 8))) {
        flash_arg[0] = I2C_ADDRESS;
        flash_arg[1] = 0;
        write_to_flash();
    } else {
        diff_load = flash_arg[2] | (flash_arg[3] << 8);
        diff_adc  = flash_arg[4] | (flash_arg[5] << 8) | (flash_arg[6] << 16) |
                   (flash_arg[7] << 24);
        if (diff_adc == 0) return;
        if (diff_load == 0) return;
        g_scale = (double)diff_adc / (double)diff_load;
        g_scale = g_scale / 100.0;
    }
}
